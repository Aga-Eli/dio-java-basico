package aula_de_visibilidade_dos_recursos;

public class Aprendizado {
    /*
     * Quando um método está em modo "default" significa que aquele método
     * so pode ser visivel ou chamado por classes que estejam no mesmo 
     * package que ele
     */

    /*
     * Quando um método está em modo "private" significa que apenas a classe
     * em que ele se encontra consegue visualizar ou chamar aquele método,
     * ou seja, apenas a classe que possui o método consegue utilizá-lo.
     * Lembre-se que um metodo pode chamar outor metodo, então não neces-
     * sariamente, aquele método precisa estar visivel a todos, ele pode 
     * apenas ser chamado dentro da propria classe que o criou dentro de 
     * outro metodo
     */

    /*
     * Por fim, quando um método está no modo "public", ele pode ser visível
     * e acessado por qualquer classe
     */
}
